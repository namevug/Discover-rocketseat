
// Para vermos o tipo dessa variável podemos utilizar a função console.log(typeof variavel)
// Variáveis e tipos de dados
// Declaração or declaration
var name

// assignment or atribuição de valore
name = "Mike"

// que tipo de dado foi colocado na variável
console.log(typeof name)

// Agrupamento de declarações
let age, isHuman

age = 20
isHuman = true

// Multiplos argumentos na funcao
console.log(name, age, isHuman)

// Escrita de texto + variaveis

// Concatenando os valores
console.log('O ' + name + ' tem ' + age + ' anos.')

// Interpolando valores com template literals or template string
console.log(`O ${name} tem ${age} anos.`)